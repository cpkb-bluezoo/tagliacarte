//! MIME parameter (name=value) for Content-Type, Content-Disposition.

#[derive(Debug, Clone)]
pub struct Parameter {
    pub name: String,
    pub value: String,
}

impl Parameter {
    pub fn new(name: impl Into<String>, value: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            value: value.into(),
        }
    }

    pub fn get_name(&self) -> &str {
        &self.name
    }

    pub fn get_value(&self) -> &str {
        &self.value
    }
}
